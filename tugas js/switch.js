var nama =  prompt('masukkan nama musim : \n (ex: dingin, gugur, dll)');

switch( nama ) {
    case 'dingin' :
        alert('Musim dingin atau musim salju merupakan saat paling dingin di wilayah beriklim kutub dan beriklim sedang ataupun subtropis.');
        break;
    case 'semi' :
        alert('musim semi adalah peralihan dari musim dingin ke musim panas.');
        break;
    case 'panas' :
        alert('Musim panas adalah salah satu musim yang memiliki ciri suhu sedang.');
        break;
    case 'gugur' :
        alert('Musim gugur adalah musim yang ditandai dengan gugurnya daun.');
        break;
}